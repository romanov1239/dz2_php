<?php

function transliterate ($inputString)
{
    $alphabet = [
        'а' => 'a', 'б' => 'b', 'в' => 'v',
        'г' => 'g', 'д' => 'd', 'е' => 'e',
        'ё' => 'e', 'ж' => 'zh', 'з' => 'z',
        'и' => 'i', 'й' => 'y', 'к' => 'k',
        'л' => 'l', 'м' => 'm', 'н' => 'n',
        'о' => 'o', 'п' => 'p', 'р' => 'r',
        'с' => 's', 'т' => 't', 'у' => 'u',
        'ф' => 'f', 'х' => 'h', 'ц' => 'c',
        'ч' => 'ch', 'ш' => 'sh', 'щ' => 'sch',
        'ь' => '\'', 'ы' => 'y', 'ъ' => '\'',
        'э' => 'e', 'ю' => 'yu', 'я' => 'ya'
    ];

    $outputString = '';

    for ($i = 0; $i < mb_strlen ($inputString); $i++) {
        $symbol = mb_substr ($inputString, $i, 1);
        $outputString = $outputString . (isset($alphabet[$symbol]) ? $alphabet[$symbol] : $symbol);
    }

    return $outputString;
}

echo transliterate ('геекбраинс');
